class Contact:
    def __init__(self, first_name, middle_name, last_name, nickname, title, company, company_address, home_telephone,
                 mobile_telephone, work_telephone, fax, email1, email2, email3, homepage, address,
                 home, notes):
        self.first_name = first_name
        self.middle_name = middle_name
        self.last_name = last_name
        self.nickname = nickname
        self.title = title
        self.company = company
        self.company_address = company_address
        self.home_telephone = home_telephone
        self.mobile_telephone = mobile_telephone
        self.work_telephone = work_telephone
        self.fax = fax
        self.email1 = email1
        self.email2 = email2
        self.email3 = email3
        self.homepage = homepage
        self.address = address
        self.home = home
        self.notes = notes
